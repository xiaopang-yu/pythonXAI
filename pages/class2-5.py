# list 串列

L1 = []
L2 = ["蘋果"]
L3 = ["蘋果", "香蕉", "橘子"]
L4 = [1, 2, 3, 4, 5]
L5 = ["蘋果", 1, True, 3.14]
L6 = [1, 2, 3, ["蘋果", "香蕉"]]  # 串列的元素也可以是串列

print(L1)
print(L2)
print(L3)
print(L4)

print(L6[2])  # 印出 3
print(L6[3])  # 印出 ["蘋果", "香蕉"]
print(L6[3][0])  # 印出 "蘋果"
print(L6[3][1])  # 印出 "香蕉"


L = [1, 2, 3, "a", "b", "c"]
print(L[1:4:2])
print(L[0:4])
print(L[::2])
